package com.yashun.bean;

public class AutoBean {
	String id;
	String maxuser;
	String nownum;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getMaxuser() {
		return maxuser;
	}
	public void setMaxuser(String maxuser) {
		this.maxuser = maxuser;
	}
	public String getNownum() {
		return nownum;
	}
	public void setNownum(String nownum) {
		this.nownum = nownum;
	}

}
